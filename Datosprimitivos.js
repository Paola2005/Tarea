//Número (Number): Representa valores numéricos, ya sean enteros o decimales.
let numero1 = 10;
let numero2 = 5.3;
console.log(typeof(numero1));
console.log(typeof numero2);


//Cadena (String): Representa secuencias de caracteres (texto).
let texto = "Bonito día para programar.";
console.log(typeof(texto));


//Booleano (Boolean): Representa valores de verdadero (`true`) o falso (`false`).
let existo = true;
console.log(typeof(existo));


//Símbolo (Symbol): Representa un valor único y se usa comúnmente como clave de propiedad en objetos
let sym1 = Symbol();
let sym2 = Symbol("foo");
let sym3 = Symbol("foo");


//Nulo (Null): Representa la ausencia intencionada de cualquier valor.
let vacia = null;
console.log(vacia);


//Indefinido (Undefined): Representa una variable que se ha declarado pero no se ha asignado un valor.
let variableSinDefinir;
console.log(variableSinDefinir);

//Objeto (Object): Es un tipo de dato complejo que puede contener propiedades y métodos.

let objeto = new Object();
console.log(objeto);




